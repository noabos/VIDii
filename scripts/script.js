//SKYSCRAPER 1
document.getElementById('btn1').addEventListener('click', function () {
    let spidey1 = document.querySelector('.spidey1');
    spidey1.classList.add('slinger');
    spidey1.addEventListener("animationend", () => {
        console.log = "let's go again!!!";
        spidey1.classList.remove('slinger');
    });
});


var textbox = document.querySelector("#textbox1");
var button = document.querySelector("#btn1");

button.addEventListener ("click", message1);

function message1 ()
{
    document.getElementById("textbox1").innerHTML = "This white and blue suit gave Spiderman enhanced strength, speed and telekenises";
};

setTimeout(() => {
  const box = document.getElementById('textbox1');
  box.style.display = 'none';
}, 7000);


